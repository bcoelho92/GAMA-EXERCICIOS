
# 1 - Faça um Programa que leia um vetor de 5 números inteiros e mostre-os.

vetor = [10,5,15,20,35]
# lista = posição [0,1,2,3,4] 

s = 0 # Contador e posição na lista
while s < len(vetor): # determina a contagem de acordo com o tamanho da lista 
    print(vetor[s]) # imprime o numero de cordo com a posição na lista
    s = s + 1   # Adicona +1 ao contador e a posição a ser impressa
